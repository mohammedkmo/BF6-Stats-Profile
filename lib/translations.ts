export type TranslationKey = keyof typeof translations.en;

export function getTranslation(locale: LanguageCode, key: TranslationKey, params?: Record<string, string>): string {
  const localeTranslations = translations[locale as keyof typeof translations];
  const translation = (localeTranslations?.[key] || translations.en[key] || key) as string;
  
  if (params) {
    return Object.entries(params).reduce(
      (text, [paramKey, paramValue]) => text.replace(`{${paramKey}}`, paramValue),
      translation
    );
  }
  
  return translation;
}
